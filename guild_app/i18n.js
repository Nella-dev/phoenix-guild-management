  // ─── applyLanguage ─────────────────────────────────────────────────────────
  window.applyLanguage = function () {
    const lang = window.currentLang;
    const dict = window.i18n[lang] || window.i18n['en'];
    if (!dict) return;

    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.getAttribute("data-i18n");
      const val = dict[key] || window.i18n['en']?.[key] || window.i18n['ko']?.[key];
      if (val) {
        if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
          el.placeholder = val;
        } else {
          el.textContent = val;
        }
      }
    });

    // langBtn 텍스트 업데이트
    const langLabels = {
      ko:'🌐 KO', en:'🌐 EN', fr:'🌐 FR', de:'🌐 DE',
      it:'🌐 IT', es:'🌐 ES', 'zh-CN':'🌐 CN', 'zh-TW':'🌐 TW',
      ja:'🌐 JA', pt:'🌐 PT'
    };
    const langBtn = document.getElementById("langBtn");
    if (langBtn) langBtn.textContent = langLabels[lang] || '🌐';
  };

  // ─── 언어 드롭다운 열기/닫기 ──────────────────────────────────────────────
  window.openLangDropdown = function () {
    let dropdown = document.getElementById('langDropdown');
    if (dropdown) {
      dropdown.classList.toggle('open');
      return;
    }
    // 드롭다운 동적 생성
    const LANGS = [
      { code:'ko',    label:'한국어',    flag:'🇰🇷' },
      { code:'en',    label:'English',   flag:'🇺🇸' },
      { code:'fr',    label:'Français',  flag:'🇫🇷' },
      { code:'de',    label:'Deutsch',   flag:'🇩🇪' },
      { code:'it',    label:'Italiano',  flag:'🇮🇹' },
      { code:'es',    label:'Español',   flag:'🇪🇸' },
      { code:'zh-CN', label:'简体中文',  flag:'🇨🇳' },
      { code:'zh-TW', label:'繁體中文',  flag:'🇹🇼' },
      { code:'ja',    label:'日本語',    flag:'🇯🇵' },
      { code:'pt',    label:'Português', flag:'🇵🇹' },
    ];

    dropdown = document.createElement('div');
    dropdown.id = 'langDropdown';
    dropdown.style.cssText = `
      position:fixed; top:80px; right:12px; z-index:9999;
      background:#1c1f2a; border:1px solid #3a4252;
      border-radius:12px; box-shadow:0 8px 32px rgba(0,0,0,.6);
      overflow:hidden; min-width:160px;
      animation: fadeInDown .18s ease;
    `;

    // 애니메이션 스타일
    if (!document.getElementById('langDropdownStyle')) {
      const st = document.createElement('style');
      st.id = 'langDropdownStyle';
      st.textContent = `
        @keyframes fadeInDown { from{opacity:0;transform:translateY(-8px)} to{opacity:1;transform:translateY(0)} }
        #langDropdown .lang-item { display:flex; align-items:center; gap:10px; padding:11px 16px; cursor:pointer; font-size:13px; color:#ccc; transition:background .15s; border-bottom:1px solid rgba(255,255,255,.04); }
        #langDropdown .lang-item:last-child { border-bottom:none; }
        #langDropdown .lang-item:hover { background:rgba(255,255,255,.07); color:#fff; }
        #langDropdown .lang-item.active { background:rgba(244,196,48,.12); color:#f4c430; font-weight:700; }
        #langDropdown .lang-item .lang-flag { font-size:16px; }
      `;
      document.head.appendChild(st);
    }

    LANGS.forEach(l => {
      const item = document.createElement('div');
      item.className = 'lang-item' + (window.currentLang === l.code ? ' active' : '');
      item.innerHTML = `<span class="lang-flag">${l.flag}</span><span>${l.label}</span>`;
      item.addEventListener('click', () => {
        window.setLanguage(l.code);
        dropdown.remove();
      });
      dropdown.appendChild(item);
    });

    document.body.appendChild(dropdown);
    dropdown.classList.add('open');

    // 외부 클릭 시 닫기
    setTimeout(() => {
      document.addEventListener('click', function closeDrop(e) {
        if (!dropdown.contains(e.target) && e.target.id !== 'langBtn') {
          dropdown.remove();
          document.removeEventListener('click', closeDrop);
        }
      });
    }, 0);
  };

  // ─── 언어 설정 ─────────────────────────────────────────────────────────────
  window.setLanguage = function(code) {
    const SUPPORTED = ['ko','en','fr','de','it','es','zh-CN','zh-TW','ja','pt'];
    if (!SUPPORTED.includes(code)) return;
    window.currentLang = code;
    localStorage.setItem("roider_lang", code);

    window.applyLanguage();

    // 페이지별 동적 리스트 갱신
    if (typeof loadMembers === "function") loadMembers();
    if (typeof renderTable === "function") renderTable();
    if (typeof renderUsers === "function") renderUsers();
    if (typeof loadAdminAttendanceRecords === "function") loadAdminAttendanceRecords();
    if (typeof allRecords !== 'undefined' && typeof renderRecord === 'function' && allRecords.length > 0) {
      renderRecord(currentIndex);
    }
  };

  // ─── handleLangChange (버튼 클릭 → 드롭다운 열기) ─────────────────────────
  window.handleLangChange = function() {
    window.openLangDropdown();
  };

  // ─── toggleLanguage (기존 호환성 유지 → 드롭다운으로 대체) ────────────────
  window.toggleLanguage = function () {
    window.openLangDropdown();
  };

  document.addEventListener("DOMContentLoaded", () => {
    window.applyLanguage();
  });
